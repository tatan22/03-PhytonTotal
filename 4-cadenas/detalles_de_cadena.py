'''
Detalles de cadena
    los caracteres de una cadena están indexados, lo que significa que cada carácter tiene una posición única.
    Por lo tanto, podemos acceder cada caracter indicando en indice del caacter que deseamos recuperar.
'''
# Manejo de indices en una cadena
cadena1 = "Hola, mundo!"
print(cadena1)

#recuperar el primer caracter
primer_caracter = cadena1[0]
print('Primer caracter:', primer_caracter) #recuperar el primer caracter H
# recuperar el último caracter
ultimo_caracter = cadena1[-1]
print('Último caracter:', ultimo_caracter) #recuperar el último caracter !
